package com.example.ligas14estadisticas;

public class Calciofechaj2Activity {
	
	private String id;
	private float ptos;
	private String nombre;
	private int bandera;
	
	public int getBandera() {
		return bandera;
	}
	public void setBandera(int bandera) {
		this.bandera = bandera;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public float getPtos() {
		return ptos;
	}
	public void setPtos(float pptos) {
		this.ptos = pptos;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

}
